export function generateMessages(params: GenerateParams): GeneratedMessage[] {
  const { category, formality, relationship } = params;
  const template = TEMPLATES[category];
  if (!template) return [];

  const messages = template.messages[formality]?.[relationship] ?? [];
  const ribbons = template.ribbons;

  return messages.map((msg, i) => ({
    ribbon: ribbons[i % ribbons.length],
    full: msg,
  }));
}

export function getCategoryInfo(slug: string): CategoryInfo | undefined {
  return CATEGORIES.find((c) => c.slug === slug);
}
